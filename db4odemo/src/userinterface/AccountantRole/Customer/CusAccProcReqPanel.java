    private void processbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = RequestTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this, "Please make selection");
            return;
        }
        else{
             Order o = (Order)RequestTable.getValueAt(selectedRow,0);
              CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("MakeOrderDecision", new CusAccountantProcessRequest(userProcessContainer, userAccount, organization,enterprise, o));
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_processbtnActionPerformed

      private void backAction(){
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }

