    public void populateRequestTable(){
        
        DefaultTableModel model = (DefaultTableModel)RequestTable.getModel();
        
        model.setRowCount(0);
       
       for(Order o : enterprise.getCol().getCustomerOrderList()){
          Object[] row = new Object[model.getColumnCount()];
          
          row[0] = o;
          
          row[1] = o.getReceiver();
    
          row[2] = o.getStatus();
         
          row[3] = o.getMessage();
          
          model.addRow(row);
       }
       
       
    }
    
    
