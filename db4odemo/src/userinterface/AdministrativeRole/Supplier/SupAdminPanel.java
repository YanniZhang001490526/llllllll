    public void populateProductTable(){
    DefaultTableModel model = (DefaultTableModel) prodTbl.getModel();
    DefaultTableCellRenderer cellRender = new DefaultTableCellRenderer(); 
    model.setRowCount(0);
    for (Product product : enterprise.getProductDir().getProductList()){
        Object[] row = new Object[4];
        row[0] = product.getProductID();
        row[1] = product;//.getProdName();
        row[2] = product.getSupplierPrice();
        row[3] = product.getAvail();
        model.addRow(row);
        
        if (product.getAvail()<5) {
            cellRender.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "The product will out of stock! ");
        }

    } 
}
    public void populateOrderTable(){
        DefaultTableModel model = (DefaultTableModel) orderTbl.getModel();
        model.setRowCount(0);
        for(Order order : enterprise.getSupplierOrderList().getSupplierOrderList()){
            Object[] row = new Object[7];
            row[0] = order.getOrderId();
            row[1] = order.getProduct();
            row[2] = order.getPrice();
            row[3] = order.getQuantity();
            row[4] = order.getSender();
            row[5] = order.getReceiver();
            row[6] = order.getStatus();
            model.addRow(row);
        }
    }
    
