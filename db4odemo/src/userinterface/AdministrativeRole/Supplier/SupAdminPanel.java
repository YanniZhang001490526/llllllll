    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        int row = prodTbl.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        Product p = (Product)prodTbl.getValueAt(row, 1);
        UpdateProductPanel upp = new UpdateProductPanel(userProcessContainer, p);
        userProcessContainer.add("UpdateProductPanel", upp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        int row = prodTbl.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            Product s = (Product)prodTbl.getValueAt(row, 1);
            
            enterprise.getProductDir().removeProduct(s);
            populateProductTable();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

